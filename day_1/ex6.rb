types_of_people = 10
# assign x a string and interpolate types_of_people
x = "There are #{types_of_people} types of people."
# assign binary the string of binary
binary = "binary"
# assign do_not the string don'
do_not = "don't"
# assign y to a string and interpolate binary and do_not
y = "Those who know #{binary} and those who #{do_not}."

# print x
puts x
# print y
puts y

# puts the string and interpolate x
puts "I said: #{x}."
# puts the string and interpolate y
puts "I also said: '#{y}'."

